def main():
    conn = psycopg2.connect(DATABASE_URL)
    cur = conn.cursor()

    # ── Ensure new categories exist ───────────────────────────────────────────
    cat_id_map = {}
    for cat in NEW_CATEGORIES:
        cur.execute('SELECT id FROM "Category" WHERE slug = %s', (cat["slug"],))
        row = cur.fetchone()
        if row:
            cat_id_map[cat["slug"]] = row[0]
            print(f"  ℹ  Category exists: {cat['name']}")
        else:
            cur.execute(
                '''INSERT INTO "Category" (name, slug, image, description, active, "createdAt")
                   VALUES (%s,%s,%s,%s,%s,NOW()) RETURNING id''',
                (cat["name"], cat["slug"], cat["image"], cat["description"], cat["active"]),
            )
            cat_id_map[cat["slug"]] = cur.fetchone()[0]
            conn.commit()
            print(f"  ✅ Created category: {cat['name']}")

    # Also load existing category ids
    cur.execute('SELECT id, slug FROM "Category"')
    for row in cur.fetchall():
        cat_id_map[row[1]] = row[0]

    # ── Insert missing products ───────────────────────────────────────────────
    inserted = 0
    skipped = 0
    for (slug, name, short_desc, description, cat_slug, colors, min_order, delivery_days) in MISSING_PRODUCTS:
        cur.execute('SELECT id FROM "Product" WHERE slug = %s', (slug,))
        if cur.fetchone():
            print(f"  ⏭  Exists: {name}")
            skipped += 1
            continue

        cat_id = cat_id_map.get(cat_slug)
        if not cat_id:
            print(f"  ❌ Category not found: {cat_slug} for product {name}")
            continue

        cur.execute(
            '''INSERT INTO "Product" (name, slug, description, "shortDescription", image,
               "categoryId", "minOrderQuantity", "estimatedDeliveryDays", active, "createdAt", "updatedAt")
               VALUES (%s,%s,%s,%s,%s,%s,%s,%s,%s,NOW(),NOW()) RETURNING id''',
            (name, slug, description, short_desc, "", cat_id, min_order, delivery_days, True),
        )
        product_id = cur.fetchone()[0]

        for color_name, color_code in colors:
            cur.execute(
                'INSERT INTO "ProductColor" (name, "colorCode", "productId") VALUES (%s,%s,%s)',
                (color_name, color_code, product_id),
            )

        conn.commit()
        print(f"  ✅ {name} (id={product_id}, {len(colors)} colors)")
        inserted += 1

    cur.close()
    conn.close()
    print(f"\n🎉 Done! Inserted {inserted} new products, skipped {skipped} existing.")


