def main():
    conn = psycopg2.connect(DATABASE_URL)
    cur  = conn.cursor()

    updated = 0
    missing = 0
    for slug, (short_ar, desc_ar) in TRANSLATIONS.items():
        cur.execute('SELECT id FROM "Product" WHERE slug = %s', (slug,))
        row = cur.fetchone()
        if not row:
            print(f"  ❌ Not found: {slug}")
            missing += 1
            continue
        product_id = row[0]
        cur.execute(
            'UPDATE "Product" SET "shortDescription" = %s, description = %s, "updatedAt" = NOW() WHERE id = %s',
            (short_ar, desc_ar, product_id)
        )
        conn.commit()
        print(f"  ✅ {slug}")
        updated += 1

    cur.close()
    conn.close()
    print(f"\n🎉 Done! Updated {updated} products, {missing} not found.")


if __name__ == "__main__":
    main()
